#include <string.h>
#include <stdio.h>

void	*ft_memchr(const void *str, int c, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if (*(char *)str == c)
			return ((char *)str);
		i++;
		str++;
	}
	return (0);
}
